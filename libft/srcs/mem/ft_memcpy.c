#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char *tmp1;
	char *tmp2;

	if (n == 0 || dest == src)
		return (dest);
	tmp1 = (char*)dest;
	tmp2 = (char*)src;
	while (--n)
		*tmp1++ = *tmp2++;
	*tmp1 = *tmp2;
	return (dest);
}
